let currentLang = 'en';

function applyTranslations(lang) {
  const t = translations[lang] || translations.en;

  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (t[key] !== undefined) el.textContent = t[key];
  });

  document.querySelectorAll('[data-i18n-aria]').forEach(el => {
    const key = el.getAttribute('data-i18n-aria');
    if (t[key] !== undefined) el.setAttribute('aria-label', t[key]);
  });

  // banner cookie: testo + link alla policy
  const cookieText = document.getElementById('cookie-text');
  if (cookieText) {
    cookieText.textContent = t.cookie_text + ' ';
    const a = document.createElement('a');
    a.href = 'cookies.html';
    a.textContent = t.footer_cookies;
    cookieText.appendChild(a);
  }

  document.documentElement.lang = lang;
  currentLang = lang;
  updateNavLangDisplay(lang);
}

function setLang(lang) {
  if (!translations[lang]) lang = 'en';
  try { localStorage.setItem('ap_lang', lang); } catch (e) { /* storage non disponibile */ }
  applyTranslations(lang);

  // se siamo sulle pagine policy, cambia anche la scheda visibile
  if (typeof showPolicyLang === 'function') showPolicyLang(lang);

  const splash = document.getElementById('splash');
  const wrapper = document.getElementById('site-wrapper');
  if (splash && wrapper && !splash.classList.contains('hidden')) {
    splash.classList.add('hidden');
    wrapper.hidden = false;
    setTimeout(() => {
      splash.style.display = 'none';
      if (typeof revealOnScroll === 'function') revealOnScroll();
    }, 900);
  }
}

function updateNavLangDisplay(lang) {
  const flags  = { en: '🇬🇧', it: '🇮🇹', pt: '🇧🇷' };
  const labels = { en: 'EN',  it: 'IT',  pt: 'PT'  };
  const flagEl  = document.getElementById('current-flag');
  const labelEl = document.getElementById('current-lang-label');
  if (flagEl)  flagEl.textContent  = flags[lang]  || '🇬🇧';
  if (labelEl) labelEl.textContent = labels[lang] || 'EN';
}

function toggleLangDropdown() {
  const dd  = document.getElementById('lang-dropdown');
  const btn = document.getElementById('lang-toggle-btn');
  if (!dd) return;
  const open = dd.classList.toggle('open');
  if (btn) btn.setAttribute('aria-expanded', open ? 'true' : 'false');
}

document.addEventListener('click', (e) => {
  const dd  = document.getElementById('lang-dropdown');
  const btn = document.getElementById('lang-toggle-btn');
  if (dd && dd.classList.contains('open') &&
      !dd.contains(e.target) && btn && !btn.contains(e.target)) {
    dd.classList.remove('open');
    btn.setAttribute('aria-expanded', 'false');
  }
});

document.addEventListener('keydown', (e) => {
  if (e.key !== 'Escape') return;
  const dd = document.getElementById('lang-dropdown');
  const btn = document.getElementById('lang-toggle-btn');
  if (dd) { dd.classList.remove('open'); if (btn) btn.setAttribute('aria-expanded', 'false'); }
  if (typeof closeMobileNav === 'function') closeMobileNav();
});

function getSavedLang() {
  let saved = null;
  try { saved = localStorage.getItem('ap_lang'); } catch (e) { /* storage non disponibile */ }
  return (saved && translations[saved]) ? saved : null;
}
